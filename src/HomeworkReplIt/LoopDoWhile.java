package HomeworkReplIt;

public class LoopDoWhile {
    public static void main(String[] args) {
        for(int i=0; i<5;i--){
            System.out.println("hello world");
            break;
        }
int z = 5;
        for(int i =5; i >0; i--){
            z += i;
            System.out.println(z);
        }
    }
}
