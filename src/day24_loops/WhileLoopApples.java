package day24_loops;

public class WhileLoopApples {
    public static void main(String[] args) {
        int apples = 1;
        //until 10
        while (apples<=10){
            System.out.println("Eating apples by one - " + apples);
            apples++;
        }
        System.out.println("no more apples");
    }
}
